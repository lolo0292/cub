#include "libft.h"
#include <stdio.h>
#include <stdlib.h>
#include <strings.h>
#include <unistd.h>

// efface les donnees des n octets de la memoire a partr de *s + remplace par \0
void	ft_bzero(void *s, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		*(unsigned char *)(s + i) = 0;
		i++;
	}
}
